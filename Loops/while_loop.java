package Loops;

public class while_loop {
    public static void main(String[] args) {
        int a = 1;
        while (a <= 5) {
            System.out.println("Hello World");
            a++;
        }

        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}
